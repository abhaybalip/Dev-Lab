-- Postgree SQL - System catlog 

-- These catalogs store metadata about databases, tables, columns, indexes, users, and other database objects:

-- pg_database: Contains information about databases.
-- pg_catalog: Stores system-wide catalog relations.
-- pg_class: Stores information about relations (tables, indexes, views, sequences, etc.).
-- pg_attribute: Stores information about columns of relations.
-- pg_index: Stores information about indexes.
-- pg_user: Stores information about database users.
-- pg_namespace: Stores information about schemas.
